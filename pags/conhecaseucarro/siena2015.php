            	<h1>Tudo sobre o Siena 2015</h1>
                <p>O modelo se mantém no mercado causando boa impressão, unindo sofisticação e design. Entre os itens que atraem os olhares para o Siena EL, temos os faróis dianteiros com máscara negra, faróis de neblina, rodas de liga leve e spoiler na tampa traseira da cor do veículo.</p>

				<p>O design interno do novo Siena EL está diferente. O quadro de instrumentos conta com conta-giros e um desenho mais moderno. As portas e os bancos ganharam revestimentos mais requintados. O modelo, agora, tem apoio para os pés e volante com sistema de absorção e regulagem de altura, garantias de conforto e praticidade para o motorista. Há, ainda, ótimas opções de rádio à escolha do cliente.</p>

			   <p>O computador de bordo do novo Siena EL tem o sistema My Car Fiat, que é um conjunto de comandos para individualizar funções básicas do carro, como, por exemplo, ativar e definir o alerta de limite de velocidade, ajustar o relógio, escolher a unidade de distância a ser exibida (km/h ou mph) e ajustar o volume dos avisos sonoros. Através desses alertas é possível controlar o limite de velocidade. Muita comodidade para manter o novo Siena EL sempre em perfeito estado.</p>
                  
                   <p>O Siena EL 2015 conta em sua lista de itens de série com Air Bag Duplo, que aumenta a segurança do motorista e passageiro ao seu lado em caso de colisão. Uma ótima opção, em conjunto com o Air Bag Duplo, são os Freios ABS com EBD, que realizam a distribuição eletrônica de frenagem do veículo, garantindo ainda mais segurança em caso de freadas bruscas. </p>
