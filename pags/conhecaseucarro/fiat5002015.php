            	<h1>Tudo sobre o Fiat 500 2015</h1>
                <p>
Sucesso de vendas desde o seu lançamento, o Fiat 500 sempre foi famoso pelo seu belo design. A versão 2015 mantém todos os elementos que arrancam suspiros dos apreciadores de um belo carro. 
				<br>
                O logo exclusivo, o teto solar elétrico Sky Wind® e seu design italiano cuidadosamente trabalhado fazem desse pequeno carro um gigante.</p>

				<p>Todo Fiat 500 é único. Além das vastas possibilidades de cores externas, você também pode personalizar o seu carro com os acessórios faixas para capô, teto e laterais. Até a capa para a sua chave pode ser customizada como o seu carro. </p>

			    <p>
Um exterior bonito não é o suficiente para fazer de um carro sinônimo de beleza. O Fiat 500 conta com trabalhos minuciosos para o seu interior, como bancos com revestimento bicolor que combinam com o revestimento do volante. Para o Fiat 500, design é coisa séria.</p>

                 <p>O que já era bom, ficou melhor. Em 2011, o motor 1.4 16v MULTIAIR chegou ao Brasil com o Fiat 500 e ganhou o prêmio de melhor motor do ano da revista Autoesporte. Agora, ele chega renovado com a tecnologia Flex, trazendo mais potência, passando de 105 para 107cv, e reduzindo ainda mais a emissão de CO² no meio ambiente. Este é o primeiro motor Multiair flex e já possui 9 prêmios ao redor do mundo. </p>
                 
                  <p>6 velocidades. 4 modos de funcionamento.
Conversor de torque com lock-up de última geração. Máximo conforto ao dirigir com menos consumo de combustível. Tudo isso em um câmbio tão completo quanto o carro. </p>

