            	<h1>Tudo sobre o Weekend 2015</h1>
                <p>O Novo Palio Weekend chega em sua versão 2015 com excelentes atributos. Continua confortável, com belos revestimentos, um painel moderno, vários porta-objetos e muitos outros detalhes que garantem ao veículo maior comodidade e conforto.</p>

				<p>O quadro de instrumentos foi reformulado. Com um fundo preto e nova disposição agradável, composto por velocímetro, conta-giros, marcador gradual de temperatura da água e um display digital onde ficam relógio, Trip Computer, My Car Fiat e indicação de nível de combustível, tanto na versão Attractive, quanto na versão Trekking.</p>

